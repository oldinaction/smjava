package theThirdChapter;

public class Point2 {
    private double x;
    private double y;

    Point2(double x1, double y1) {
        x = x1;
        y = y1;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }

    public void setX(double i) {
        x = i;
    }

    public void setY(double i) {
        y = i;
    }

}
